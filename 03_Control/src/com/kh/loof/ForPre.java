public class ForPre {
    public void method1(){
        String[] fruits = {"Apple", "Banana", "Orange"};
        for (String fruit : fruits) {
            System.out.println(fruit);
        }
    }

    public void method2(){
        int[] numbers = {1, 2, 3,4,5};
        for (int number : numbers) {
            System.out.println(number);
        }
    }
}
